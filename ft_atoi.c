int	ft_atoi(const char *str)
{
	int	num;
	int	neg;

	num = 0;
	while (*str == ' ' || *str == '\t')
		++str;
	if (*str == '+' && ++str)
		neg = 1;
	else if (*str == '-' && ++str)
		neg = -1;
	while (*str >= '0' && *str <= '9')
		num = num * 10 + (*str - '0');
	return (neg * num);
}
